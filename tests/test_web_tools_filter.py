def test_standalone_and_embedded_web_tools_filter_are_identical():
    """The standalone reference filter must equal the generated embedded source.

    Guards against drift: any field added to one copy must be added to the other.
    """
    standalone = _STANDALONE_PATH.read_text(encoding="utf-8")
    embedded = FilterManager.render_openrouter_web_tools_filter_source(
        enable_web_search=True,
        enable_web_fetch=True,
        enable_datetime=True,
    )
    assert embedded.strip() == standalone.strip()
