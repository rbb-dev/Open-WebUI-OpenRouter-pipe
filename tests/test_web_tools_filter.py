def test_no_server_tools_written_when_all_toggles_off():
    """Access model: with every per-tool toggle off, the filter writes NO server tools.

    Tool access is gated by filter attach + per-tool toggles (admins grant the filter;
    users flip toggles) — not by model capability. Nothing enabled => nothing injected.
    """
    module = _embedded_module()
    user_valves = module.Filter.UserValves(WEB_SEARCH=False, WEB_FETCH=False, DATETIME=False)
    meta: dict = {}
    module.Filter().inlet({}, __metadata__=meta, __user__={"valves": user_valves})
    assert not meta.get("openrouter_pipe", {}).get("server_tools")


